int Entity_Collision(Entity a, Entity b){
	if((a.x <= b.x && a.x+a.image->w > b.x) || (b.x <= a.x && b.x+b.image->w > a.x))
		if((a.y <= b.y && a.y+a.image->h > b.y) || (b.y <= a.y && b.y+b.image->h > a.y))
			return true;
  return false;
}

int Check_Box_Hitbox(Entity e){
  for(int i=0; i<NUM_BLOCKS; i++){
    if(Entity_Collision(e, blocks[i].entity))
      return i;
  }
  return -1;
}

int Check_Enemy_Hitbox(Entity e){
	for(int i=0; i<NUM_ENEMIES; i++){
		if(Entity_Collision(e, enemies[i].entity))
			return i;
	}
	return -1;
}

