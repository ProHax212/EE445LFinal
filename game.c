void Goomba_Move(int g){
	if(enemies[g].entity.image != &goomba_smash){//If not smashed
		int dir = 1;
		if(enemies[g].entity.mirror)
			dir = -1;
		for(int i=GOOMBA_SPEED; i>0; i++){//LEFT RIGHT Goomba hitbox detection
			enemies[g].entity.x += GOOMBA_SPEED * dir;
			if(Check_Box_Hitbox(enemies[g].entity) != -1){
				enemies[g].entity.x -= i*dir;
				enemies[g].entity.mirror = !enemies[g].entity.mirror;
			} else {
				int otherEnemy = Check_Enemy_Hitbox(enemies[g].entity);
				if(otherEnemy != g){
					enemies[g].entity.x -= i*dir;
					enemies[g].entity.mirror = !enemies[g].entity.mirror;
					enemies[otherEnemy].entity.mirror = !enemies[otherEnemy].entity.mirror;
				}
				break;
			}
		}
		for(int i=0; i<GRAVITY; i++){//Add Gravity
			enemies[g].dy -= (GRAVITY-i);
			enemies[g].entity.y += enemies[g].dy;
			if(Check_Box_Hitbox(enemies[g].entity) != -1){//Hit floor
				enemies[g].entity.y -= enemies[g].dy;
				enemies[g].dy += (GRAVITY-i);
			}
		}
		if(Check_Enemy_Hitbox(mario.entity) == g && mario.entity.y <= enemies[g].entity.y && mario.refresh == 0){//Check if should hurt mario
			mario.hp--;
			mario.refresh = REFRESH;
			Update_Mario_Sprite();
		}
	} else {//Remove from screen
		enemies[g].smashCounter--;
		if(enemies[g].smashCounter == 0)
			enemies[g].entity.x = 0-enemies[g].entity.image->w;
	}
}

void Shroom_Move(int s){
		int dir = 1;//Right
		if(shrooms[s].entity.mirror)
			dir = -1;//Left
		for(int i=GOOMBA_SPEED; i>0; i++){//LEFT RIGHT Shroom hitbox detection
			shrooms[s].entity.x += GOOMBA_SPEED * dir;
			if(Check_Box_Hitbox(shrooms[s].entity) != -1 || Check_Enemy_Hitbox(shrooms[s].entity) != -1){
				shrooms[s].entity.x -= i*dir;
				shrooms[s].entity.mirror = !shrooms[s].entity.mirror;
			} else break;
		}
		for(int i=0; i<(-1*shrooms[s].dy)+GRAVITY; i++){//Add Gravity
			shrooms[s].dy -= (GRAVITY-i);
			shrooms[s].entity.y += shrooms[s].dy;
			if(Check_Box_Hitbox(shrooms[s].entity) != -1){//Hit floor
				shrooms[s].entity.y -= shrooms[s].dy;
				shrooms[s].dy += (GRAVITY-i);
			}
		}
		if(Entity_Collision(shrooms[s].entity, mario.entity)){
			shrooms[s].eaten = true;
			shrooms[s].entity.x = -1*shrooms[s].entity.image->w;
			if(mario.hp < 2)
				mario.hp = 2;
				Update_Mario_Sprite();
		}
}

void Enemy_Update(void){
	for(int i=0; i<NUM_ENEMIES; i++){
		Goomba_Sprite_Cycle(i);
		if(enemies[i].entity.x < mario.entity.x+SCREEN_W-INIT_X)
			Goomba_Move(i);
	}
}

void Shroom_Update(void){
	for(int i=0; i<shroomPointer; i++){
		if(!shrooms[i].eaten)
			Shroom_Move(i);
	}
}

