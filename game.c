Sound Player_Update(Input input){
	Sound sound;
	if(mario.refresh > 0)
		mario.refresh--;
	if(mario.entity.x >= CASTLE_X + FLAG_X_OFFSET-6){//Check if touching flag pole
		gameOver = true;
		mario.entity.image = &mario_flag[mario.hp-1];
		sound = WIN;
	}
	if(mario.entity.y < 0-mario.entity.image->h && !gameOver){//Check if fell off world
		gameOver = true;
		sound = DIE;
	} else if(mario.hp < 1) {//Died
		if(!gameOver){
			gameOver = true;
			mario.dy = 16;//Launch mario
			sound = DIE;
			mario.entity.image = &mario_hit;
		}
		mario.entity.y += mario.dy;
		mario.dy-=GRAVITY;
	}
	if(!gameOver){
		sound = Player_Input(input);
	} else {//Game over, lock player controls ^
		if(mario.entity.y > base_block.h+strong_block.h)//Mario pole slide
			mario.entity.y -= POLE_SLIDE_SPEED;
		if(scenery[flagPointer].entity.y > base_block.h+strong_block.h)//Flag pole slide
			scenery[flagPointer].entity.y -= POLE_SLIDE_SPEED;
	}
	return sound;
}

//Source: entity
//Destination: img
bool draw(Entity* entity, Frame* img){
  bool onScreen = false;
  for(int r=0; r<entity->image->h; r++){//For each row in the entity
    int y = entity->y + r;
    if(y < SCREEN_H && y >= 0){
      for(int c=0; c<entity->image->w; c++){//For each column in the row
        int left = mario.entity.x - INIT_X;
        int right = left + SCREEN_W;
        int x = entity->x + c;
        if(left <= x && x < right){//If pixel is on screen
          onScreen = true;
          int yOffset = img->w * (r+entity->y);
          char pixel;
          if(entity->mirror)
            pixel = entity->image->pixels[(entity->image->w*(r+1)) - c - 1];
          else
            pixel = entity->image->pixels[(entity->image->w*r) + c];
          if(getOpacity(pixel)){
						//if((img->pixels[yOffset + x - left]&0x7f) == (pixel&0x7f))//Hasn't changed
							//img->pixels[yOffset + x - left] &= 0x7f;
						//else
							img->pixels[yOffset + x - left] = pixel;//Draw on image
					}
        }
      }
    }
  }
  return onScreen;
}

//Updates gameR and returns frame (160x128)
Frame Next_Frame(Input input){
  //Init frame
  Frame frame;
  frame.h = SCREEN_H;
  frame.w = SCREEN_W;

	frame.sound = Player_Update(input);
  Enemy_Update();
	Question_Block_Update();
	Shroom_Update();

  //Draw sky layer
  for(int i=0; i<SCREEN_H*SCREEN_W; i++)
		sky_pixels[i] = SKY;
	frame.pixels = sky_pixels;
  //Draw scenery layer
  for(int i=0; i<sizeof(scenery)/sizeof(scenery[0]); i++)
    draw(&scenery[i].entity, &frame);
  //Draw block layer
  for(int i=0; i<sizeof(blocks)/sizeof(blocks[0]); i++)
    draw(&blocks[i].entity, &frame);
	//Draw shrooms
	for(int i=0; i<shroomPointer; i++)
		draw(&shrooms[i].entity, &frame);
	//Draw enemies
	for(int i=0; i<NUM_ENEMIES; i++)
		draw(&enemies[i].entity, &frame);
	//Draw mario
  draw(&mario.entity, &frame);
  return frame;
}

